public class Variables_Enteros {
    public static void main(String[] args) {
        
    // Variables de tipo entero
    byte num_1 = -128; // desde -128 hasta 127 
    short num_2 = -32000; // desde -32 mil hasta 32 mil
    int num_3 =  299999999; // desde - 2,000,000,000 hasta 2, positivo
    long num_4 = 900000000;

    // Variables de tipo decimal
    float num_5 = 12.8988888999f;
    double num_6 = 7.9094858888888;

    // Variables de tipo verdadero o falso
    boolean estado = false;

    // Variables de tipo char
    char palabra = 'h'; // Solo para guardar un caracter ya sea letra o numero

    // Variables de tipo string osea texto palabras parrafos y mas
    String palabra2 = "hola? como estan";

    System.out.println(palabra2);


    }
}