'''
Pide al usuario que ingrese tres numeros y
Luego calcula y muestra el promedio de esos
tres numeros.
'''
# Pedir al usuario que ingrese tres números
numero1 = float(input("Ingrese el primer número: "))
numero2 = float(input("Ingrese el segundo número: "))
numero3 = float(input("Ingrese el tercer número: "))

# Calcular el promedio de los tres números
promedio = (numero1 + numero2 + numero3) / 3

# Mostrar el resultado del promedio
print("El promedio de los tres números es:", promedio)
